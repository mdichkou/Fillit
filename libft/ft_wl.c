#include "libft.h"

int	ft_wl(char *s, char c)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] != c && (s[i + 1] == c || s[i + 1] == '\0'))
			return (i);
		i++;
	}
	return (i);
}
