#include "libft.h"

int	ft_cw(char *s_copy, char c)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (s_copy[i])
	{
		if ((s_copy[i] == c && s_copy[i + 1] != c && s_copy[i + 1]) != '\0'
				|| (s_copy[0] != c && i == 0))
			j++;
		i++;
	}
	return (j);
}
